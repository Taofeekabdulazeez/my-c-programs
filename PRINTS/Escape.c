#include <stdio.h>
int main(){


printf("Ramadan Kareem");   // The statement will be printed out
printf("\nRamadan Kareem"); // The statement will be printed on a new line
printf("\0Ramadan Kareem"); // The statement won't be printed out
printf("\nRamadan\tkareem"); // This creates and horizontal tab/space between the two words
printf("\n\"Ramadan kareem\""); // This inserts a double quote
printf("\n\'Ramadan kareem\'"); // This inserts a single quote
printf("\n\\Ramadan Kareem\\"); // This inserts a backslash
printf("\n\bRamadan kareem");   // This inserts a backspace
// printf("\nshola\nnnigeria");



 return 0;
}